
public class LogicalOperators {

    public static void main(String[] args) {
        boolean a = true;
        boolean b = false;

        System.out.println("a and b is " + (a && b) + ".");
        System.out.println("a or b is " + (a || b) + ".");
        System.out.println("!a is " + !a + ".");

    }

}
