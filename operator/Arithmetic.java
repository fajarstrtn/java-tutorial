
public class Arithmetic {

    public static void main(String[] args) {
        int a = 10;
        int b = 5;

        System.out.println(a + b); // Output: 15
        System.out.println(a - b); // Output: 5
        System.out.println(a * b); // Output: 50
        System.out.println(a / b); // Output: 2
        System.out.println(a % b); // Output: 0

        int c = 7;
        int d = 4;

        System.out.println(c / d); // Output: 1
        System.out.println(c % d); // Output: 3

        double e = c / (double) d;

        System.out.println(e); // Output: 1.75

        String firstName = "John";
        String lastName = "Doe";

        System.out.println(firstName + lastName); // Output: JohnDoe
    }

}
