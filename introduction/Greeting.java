
public class Greeting {

    public static void main(String[] args) {
        /*
         * This is the first touch of learning programming language.
         * The print() and println() methods are used to display output to the console.
         * 
         * For println method, it displays the specified argument
         * to the console then adds a new line character (\n) at the end.
         * 
         * This moves the cursor to the beginning of the next line
         * for any subsequent output. */
        System.out.println("Hello World!");

        /*
         * For print method, it also displays the specified argument
         * to the console without adding a new line character at the end. */
        System.out.print("This is ");
        System.out.print("how print() works!");
    }

}
