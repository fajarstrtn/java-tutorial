
public class Comment {

    public static void main(String[] args) {
        // This is a single-line comment.
        System.out.println("Good Morning!"); // This is also a single-line comment.
        System.out.println("Good Night!");   // Yet, this is a single-line comment.

        /*
         * This is multi-line comment.
         *
         * These lines of code allow you to display output to the console. */
        System.out.println("Hello World!");
    }

}
